func renderTool(out io.Writer, c checkedTool) {
	mark := ui.MarkFail()
	if c.found {
		mark = ui.MarkSuccess()
	}
	line := fmt.Sprintf("  %s %-10s", mark, c.name)
	if c.found {
		line += "  " + ui.Muted.Render(c.version)
	} else {
		line += "  " + ui.Muted.Render(c.purpose+" — install: "+c.installHint)
	}
	fmt.Fprintln(out, line)
}
