func newOnboardingCmd() *cobra.Command {
	var interactive bool
	c := &cobra.Command{
		Use:   "onboarding",
		Short: "Detect installed agent CLIs + dev tools and print next-step recipe",
		RunE: func(cmd *cobra.Command, _ []string) error {
			dir, err := cwd()
			if err != nil {
				return err
			}
			if interactive {
				return runOnboardingInteractive(cmd.Context(), cmd.InOrStdin(), cmd.OutOrStdout(), dir)
			}
			return runOnboarding(cmd.Context(), cmd.OutOrStdout(), dir)
		},
	}
	c.Flags().BoolVar(&interactive, "interactive", false, "prompt to pin a suggested adapter and run setup actions")
	return c
}

func runOnboardingInteractive(ctx context.Context, in io.Reader, out io.Writer, root string) error {
	r := gatherOnboarding(ctx, root)
	renderOnboarding(out, r)
	reader := bufio.NewReader(in)

	offerMissingToolInstalls(ctx, reader, out, r.Tools)
	if pinErr := offerAdapterPin(ctx, reader, out, root, r.Adapters); pinErr != nil {
		return pinErr
	}
	offerMultiAdapterRoutes(reader, out, root, r.Adapters)
	offerBackupQuickstart(ctx, reader, out, root)
	offerScaffold(ctx, reader, out)
	offerCiInstallMissing(ctx, reader, out, root)
	offerMCPSetup(ctx, reader, out, root)
	offerDockerSetup(reader, out, root)
	fmt.Fprintln(out, "\n"+ui.Heading.Render("setup done")+" — open a chat with "+ui.Accent.Render("harness chat --auto-gate"))
	return nil
}

func installedAdapterIDs(adapters []checkedTool) []string {
	out := make([]string, 0, len(adapters))
	for _, a := range adapters {
		if a.found {
			out = append(out, a.name)
		}
	}
	return out
}

func offerMultiAdapterRoutes(in *bufio.Reader, out io.Writer, root string, adapters []checkedTool) {
	installed := installedAdapterIDs(adapters)
	if len(installed) < 2 {
		return
	}
	fmt.Fprintln(out, "\n"+ui.Heading.Render("multi-adapter routing"))
	fmt.Fprintln(out, "  map task tags (planning / implementation / cheap_review) to different adapters")
	fmt.Fprintln(out, "  so /plan hits a cheap model and plain text hits a stronger one.")
	if !askYesNo(in, out, "configure routes now?", true) {
		fmt.Fprintln(out, "  skipped — edit .harness/config/routes.yaml later")
		return
	}
	tasks := []string{"planning", "implementation", "cheap_review"}
	picks := map[string]string{}
	for _, tag := range tasks {
		idx, err := askChoice(in, out, "  "+ui.Accent.Render(tag)+" — pick adapter:", installed, 0)
		if err != nil {
			return
		}
		picks[tag] = installed[idx]
	}
	dir := filepath.Join(root, ".harness", "config")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		fmt.Fprintf(out, "  %s mkdir: %v\n", ui.MarkFail(), err)
		return
	}
	body := renderRoutesYAML(picks, installed)
	path := filepath.Join(dir, "routes.yaml")
	if err := os.WriteFile(path, []byte(body), 0o644); err != nil {
		fmt.Fprintf(out, "  %s write %s: %v\n", ui.MarkFail(), path, err)
		return
	}
	fmt.Fprintf(out, "  %s wrote %s\n", ui.MarkSuccess(), ui.Accent.Render(path))
}

func renderRoutesYAML(picks map[string]string, installed []string) string {
	tasks := []string{"planning", "implementation", "cheap_review"}
	var b strings.Builder
	b.WriteString("# generated by `harness onboarding --interactive`\n")
	b.WriteString("routes:\n")
	for _, tag := range tasks {
		primary, ok := picks[tag]
		if !ok {
			continue
		}
		b.WriteString("  " + tag + ":\n")
		b.WriteString("    primary: " + primary + "\n")
		fallback := []string{}
		for _, id := range installed {
			if id != primary {
				fallback = append(fallback, id)
			}
		}
		if len(fallback) > 0 {
			b.WriteString("    fallback: [" + strings.Join(fallback, ", ") + "]\n")
		}
	}
	return b.String()
}

