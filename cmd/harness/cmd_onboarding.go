func offerMCPSetup(ctx context.Context, in *bufio.Reader, out io.Writer, root string) {
	names, err := mcppkg.List()
	if err != nil || len(names) == 0 {
		return
	}
	fmt.Fprintln(out, "\n"+ui.Heading.Render("MCP servers"))
	fmt.Fprintf(out, "  bundled templates: %s\n", strings.Join(names, ", "))
	fmt.Fprintln(out, "  external search:   harness mcp search <term>  (modelcontextprotocol registry)")
	choice, err := askChoice(in, out, "  setup:", []string{
		"install all defaults (recommended)",
		"search bundled by substring (then pick a few)",
		"skip — configure later",
	}, 0)
	if err != nil {
		return
	}
	switch choice {
	case 0:
		for _, name := range names {
			if err := runHarnessSubcommand(ctx, out, root, "mcp", "install", name, "--yes"); err != nil {
				fmt.Fprintf(out, "  %s install %s: %v\n", ui.MarkFail(), name, err)
				continue
			}
			fmt.Fprintf(out, "  %s installed %s\n", ui.MarkSuccess(), ui.Accent.Render(name))
		}
	case 1:
		offerMCPSubstringPick(ctx, in, out, root, names)
	default:
		fmt.Fprintln(out, "  skipped — run `harness mcp install <name>` later")
	}
}

func offerMCPSubstringPick(ctx context.Context, in *bufio.Reader, out io.Writer, root string, names []string) {
	for {
		fmt.Fprint(out, "  filter (substring, empty to list all, 'done' to finish): ")
		line, _ := in.ReadString('\n')
		q := strings.TrimSpace(line)
		if q == "done" {
			return
		}
		filtered := []string{}
		for _, n := range names {
			if q == "" || strings.Contains(n, q) {
				filtered = append(filtered, n)
			}
		}
		if len(filtered) == 0 {
			fmt.Fprintf(out, "  %s no matches for %q\n", ui.MarkWarn(), q)
			continue
		}
		filtered = append(filtered, "done")
		idx, err := askChoice(in, out, "  pick:", filtered, len(filtered)-1)
		if err != nil || idx == len(filtered)-1 {
			return
		}
		name := filtered[idx]
		if err := runHarnessSubcommand(ctx, out, root, "mcp", "install", name, "--yes"); err != nil {
			fmt.Fprintf(out, "  %s install %s: %v\n", ui.MarkFail(), name, err)
			continue
		}
		fmt.Fprintf(out, "  %s installed %s\n", ui.MarkSuccess(), ui.Accent.Render(name))
	}
}

func offerDockerSetup(in *bufio.Reader, out io.Writer, root string) {
	stack := detectDockerStack(root)
	if stack == "" {
		return
	}
	fmt.Fprintln(out, "\n"+ui.Heading.Render("docker setup"))
	fmt.Fprintf(out, "  detected stack: %s — scaffold Dockerfile + docker-compose.yml?\n", ui.Accent.Render(stack))
	if !askYesNo(in, out, "write files?", false) {
		fmt.Fprintln(out, "  skipped")
		return
	}
	df, compose := renderDockerScaffold(stack)
	wrote := []string{}
	for _, f := range []struct {
		path string
		body string
	}{
		{filepath.Join(root, "Dockerfile"), df},
		{filepath.Join(root, "docker-compose.yml"), compose},
	} {
		if _, err := os.Stat(f.path); err == nil {
			fmt.Fprintf(out, "  %s %s already exists — skipping\n", ui.MarkWarn(), f.path)
			continue
		}
		if err := os.WriteFile(f.path, []byte(f.body), 0o644); err != nil {
			fmt.Fprintf(out, "  %s write %s: %v\n", ui.MarkFail(), f.path, err)
			continue
		}
		wrote = append(wrote, f.path)
	}
	for _, p := range wrote {
		fmt.Fprintf(out, "  %s wrote %s\n", ui.MarkSuccess(), ui.Accent.Render(p))
	}
}

func detectDockerStack(root string) string {
	probes := []struct {
		marker string
		stack  string
	}{
		{"go.mod", "go"},
		{"pyproject.toml", "python"},
		{"requirements.txt", "python"},
		{"package.json", "node"},
	}
	for _, p := range probes {
		if _, err := os.Stat(filepath.Join(root, p.marker)); err == nil {
			return p.stack
		}
	}
	return ""
}

func renderDockerScaffold(stack string) (string, string) {
	switch stack {
	case "go":
		return dockerfileGo, composeGeneric(8080)
	case "python":
		return dockerfilePython, composeGeneric(8000)
	case "node":
		return dockerfileNode, composeGeneric(3000)
	}
	return "", ""
}

